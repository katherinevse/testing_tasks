/*Давай создадим программу, 
которая находит сумму элементов массива 
с использованием указателей. 
Вот пример:
*/

#include <stdio.h>

int findSum(int *array, int size) {
    // Используем указатель для обращения к элементам массива и нахождения суммы
    int sum = 0;
    for (int i = 0; i < size; ++i) {
        // Добавляем значение, на которое указывает указатель, к сумме
        sum += *array;

        // Переходим к следующему элементу массива
        array++;
    }

    return sum;
}

int main() {
    // Пример массива
    int myArray[] = {1, 2, 3, 4, 5};
    int size = sizeof(myArray) / sizeof(myArray[0]);

    // Выводим исходный массив
    printf("Исходный массив: ");
    for (int i = 0; i < size; ++i) {
        printf("%d ", myArray[i]);
    }

    // Вызываем функцию, передавая массив и его размер
    int sum = findSum(myArray, size);

    // Выводим результат
    printf("\nСумма элементов массива: %d\n", sum);

    return 0;
}
